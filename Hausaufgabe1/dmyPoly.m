function y = dmyPoly(x)
%dmyPoly - Derivative function of the mathematical function used in myPoly 
%
% Syntax:  y = dmyPoly(x)
%
% Inputs:
%    x - x-argument of the function
%
% Outputs:
%    y - the function value corresponding to the x-argument according to the mathematical formula
%
% Other m-files required: none
% Subfunctions: none
% MAT-files required: none
%
% See also: myPoly,  myNewton
%
% MATLAB Version: MATLAB R2022a
%
% Author: Adrian Waldera
% DHBW Stuttgart
% email: adrianwaldera@gmx.de
% April 2022; Last revision: 05-April-2022

    y = 2*(x-3);
end